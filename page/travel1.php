<h4>فعاليات و أنشطة</h4>
<p>
بأتكلم عن التجارب المحلية ، الجزء التالي من رحلتي سيكون عن الأنشطة. أولها سوق المنامة. تناولت وجبة الإفطار في مطعم يدعى مقهى حاجي ، والذي كان موجودًا منذ عام 1950 ويتردد عليه البحرينيين والأجانب على حد سواء. جلسات في غرفة صاخبة ،  طلبت كل من البيض مع الطماطم إلى الأجبان المحلية والعدس الأصفر الذيذ.

