<p>

 التاريخ مباشرة في قلعة البحرين ، حيث أتسلق أبراج عمرها قرون وننزل إلى غرف كهفية. فإنه محاط بأطلال محفورة للحضارات السابقة ، وهي منذ آلاف السنين من التراث الثقافي.
 ان سبب تسمية البحرين باللؤلؤ. لانها تشتهر بالجمال الطبيعي الذي جذب الجميع.

</p>
