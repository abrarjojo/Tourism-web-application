<h3>المعالم الثقافية</h3>
<p>
بدأت اليوم الاول بزيارة متحف البحرين الوطني ، الذي يحتوي على كل ألواح الكتابة المسمارية القديمة، من حضارة إلى المتعة في الأسواق التقليدية. إنه مكان رائع للتعرف على ماضي البحرين والتمتع بطابع التاريخ. انه رائع اكثر مما كنت أتخيله
</p><br>
<img  src="../images/pic3.jpg" alt="" height="450" width="600"/>
